package com.jks.entity;

public class PolicyMeasure {
	
	private int id;
	
	private String admcode;
	
	private String year;
	
	private String city;
	
	private String AutoRight;
	
	private String Level;
	
	private int audit;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAdmcode() {
		return admcode;
	}

	public void setAdmcode(String admcode) {
		this.admcode = admcode;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getAutoRight() {
		return AutoRight;
	}

	public void setAutoRight(String autoRight) {
		AutoRight = autoRight;
	}

	public String getLevel() {
		return Level;
	}

	public void setLevel(String level) {
		Level = level;
	}

	public int getAudit() {
		return audit;
	}

	public void setAudit(int audit) {
		this.audit = audit;
	}
	
	
	
}
